typedef struct node
{
    int data;
    struct node *next;

} NODE, *PNODE, **PPNODE;

/*----------------------------------------------------------
 Old Name               New Name
 ----------------------------------------------------------
 struct Node            NODE
 struct Node *          PNODE
 struct Node **         PPNODE
 ----------------------------------------------------------*/


 /*///////////////////////////////////////////////////////////////////////////////////////////////////////////////
 /*////////////////////////////////////////////////////////////////////////////////////////////////////////////////

