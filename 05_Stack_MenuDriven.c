void Display(PNODE Head);
int Count(PNODE Head);

void Push(PPNODE Head , int no);
void Pop(PPNODE Head);


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////



//---------------------------------------------------------------
// Function Definations
//---------------------------------------------------------------
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//-----------------------------------------------------------------
// Function name : Display
// Description : Used to display List of elements
// Parameters : First pointer
// Return Value : void
//-----------------------------------------------------------------
///////////////////////////////////////////////////////////////////

void Display(PNODE Head)
{
    PNODE temp = Head;

    while (temp != NULL)
    {
        printf("|%d|->", temp->data);
        temp = temp->next;
    }

    printf("NULL\n");
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
//-----------------------------------------------------------------
// Function name : Count
// Description : Used to count elements of Linked List
// Parameters : First pointer
// Return Value : int
//-----------------------------------------------------------------
///////////////////////////////////////////////////////////////////

int Count(PNODE Head)
{
    PNODE temp = Head;
    int iCnt = 0;

    while (temp != NULL)
    {
        iCnt++;
        temp = temp->next;
    }

    return iCnt;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//-----------------------------------------------------------------
// Function name : Push
// Description : Used to insert at last position of Linked List
// Parameters : Addreas of First pointer & data of node
// Return Value : void
//-----------------------------------------------------------------
///////////////////////////////////////////////////////////////////

void Push(PPNODE Head, int no)
{
    PNODE newn = (PNODE)malloc(sizeof(NODE));

    newn->data = no;
    newn->next = NULL;

    if (*Head == NULL)
    {
        *Head = newn;
    }
    else
    {
        newn->next = *Head;
        *Head = newn;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//-----------------------------------------------------------------
// Function name : POP
// Description : Used to delete last element of Linked List
// Parameters : Addreas of First pointer
// Return Value : void
//-----------------------------------------------------------------
///////////////////////////////////////////////////////////////////

void Pop(PPNODE Head)
{
    PNODE temp = *Head;

    if (temp != NULL)
    {
        *Head = (*Head)->next;

        free(temp);
    }
}

